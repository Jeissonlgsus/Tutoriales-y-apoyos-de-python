colores = {'Red','Green','Blue'}
# esto es un "set"
# los sets son listas que no respetan orden. 
print(type(colores))
print(colores)
# para adicionar elementos usamos la funcion "add"
colores.add('Violeta')
print(colores)
# de igual modo para quitarlos usamos "remove"
colores.remove('Red')
print(colores)

